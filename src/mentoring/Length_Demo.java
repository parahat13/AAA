package mentoring;

public class Length_Demo {

	public static void main(String[] args) {

		String str = "JAVA";

		int[] numbers = { 1, 2, 3, 4, 5 };
		
		String[] abc = { "a", "b" };

		System.out.println("Length of  String: " + str.length()); // to find out length of string

		System.out.println("Size of Array : " + numbers.length); // to find out size of array

		System.out.println("Size of  Array: " + abc.length);

	}

}
